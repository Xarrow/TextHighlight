/**
 * 淡化效果的配置参数
 主要改进包括：

  配置参数化：
  
  将所有淡化效果的参数抽取到独立的配置文件
  可以轻松调整透明度、模糊度、缩放等效果
  支持精细调整过渡动画的时间和曲线
  逻辑模块化：
  
  将每个效果计算拆分为独立函数
  更清晰的代码结构和职责划分
  便于维护和调整单个效果
  渐变控制：
  
  使用距离衰减因子控制淡化范围
  通过指数曲线调整渐变过渡
  位置权重影响文本整体的渐变效果
  现在你可以通过调整 fadeConfig.js 中的参数来精确控制淡化效果：
  
  opacity: 控制文字的透明度范围
  distance: 控制距离对淡化的影响
  position: 控制位置对淡化的影响
  blur: 控制模糊效果的强度
  scale: 控制缩放效果的范围
  transition: 控制动画过渡效果

 */
